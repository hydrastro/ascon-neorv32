cat > doc/phase5_2.md <<'EOF'
# Phase 5.2 — Tang Nano 9K target profile

Phase 5.2 introduces Tang Nano 9K as the first physical bring-up board.

This phase does **not** yet create a complete NEORV32 Gowin bitstream. It establishes a conservative board profile and accelerator synthesis variants so we can answer resource-fit questions before wiring the full SoC.

## Target

```text
Board: Sipeed Tang Nano 9K
FPGA:  GW1NR-LV9QN88PC6/I5
Clock: 27 MHz onboard oscillator
```

## Principle

The board is small. Therefore the first hardware build should not use the fastest or largest accelerator.

Initial profile:

```text
single accelerator instance
DECRYPT=0
ROUNDS_PER_CYCLE=1
small FIFO defaults from the core wrapper
```

After that works:

```text
try RPC=2
try RPC=4 only if there is comfortable timing/resource margin
add decryption or dual windows only if area allows
```

## Commands

```sh
make TANG_VARIANT=small board-tangnano9k-info
make TANG_VARIANT=small synth-tangnano9k-accel
make synth-tangnano9k-matrix
```

## Exit criteria

Phase 5.2 is complete when:

```text
- the board profile files are committed
- accelerator-only synthesis matrix runs
- we choose the first NEORV32 top-level configuration for Phase 5.3
```
EOF

# Append Makefile targets if not already present.
if ! grep -q 'board-tangnano9k-info' Makefile; then
cat >> Makefile <<'EOF'

# -----------------------------------------------------------------------------
# Tang Nano 9K board-profile helpers
# -----------------------------------------------------------------------------
TANG_VARIANT ?= small
TANG_VARIANT_FILE := boards/tang-nano-9k/variants/$(TANG_VARIANT).mk
-include $(TANG_VARIANT_FILE)

.PHONY: board-tangnano9k-info synth-tangnano9k-accel synth-tangnano9k-matrix

board-tangnano9k-info:
	@test -f "$(TANG_VARIANT_FILE)" || { echo "ERROR: unknown TANG_VARIANT='$(TANG_VARIANT)'"; exit 1; }
	@echo "Board:              $${BOARD_NAME:-tang-nano-9k}"
	@echo "Variant:            $(TANG_VARIANT)"
	@echo "FPGA_DEVICE:        $(FPGA_DEVICE)"
	@echo "BOARD_CLOCK_HZ:     $(BOARD_CLOCK_HZ)"
	@echo "ACCEL_TOP:          $(ACCEL_TOP)"
	@echo "ACCEL_PROFILE_NAME: $(ACCEL_PROFILE_NAME)"
	@echo "ACCEL_DECRYPT:      $(ACCEL_DECRYPT)"
	@echo "ACCEL_RPC:          $(ACCEL_RPC)"

synth-tangnano9k-accel: check-core | $(BUILD_DIR)
	@test -f "$(TANG_VARIANT_FILE)" || { echo "ERROR: unknown TANG_VARIANT='$(TANG_VARIANT)'"; exit 1; }
	$(YOSYS) -p 'read_verilog -sv $(RTL_FILES); chparam -set DECRYPT $(ACCEL_DECRYPT) ascon_aead128_xbus; chparam -set ROUNDS_PER_CYCLE $(ACCEL_RPC) ascon_aead128_xbus; synth -top ascon_aead128_xbus; stat -top ascon_aead128_xbus' > $(BUILD_DIR)/yosys_tangnano9k_$(TANG_VARIANT)_accel.txt
	cat $(BUILD_DIR)/yosys_tangnano9k_$(TANG_VARIANT)_accel.txt

synth-tangnano9k-matrix:
	$(MAKE) TANG_VARIANT=small synth-tangnano9k-accel
	$(MAKE) TANG_VARIANT=medium synth-tangnano9k-accel
	$(MAKE) TANG_VARIANT=fast-if-fits synth-tangnano9k-accel
	$(MAKE) TANG_VARIANT=decrypt-small synth-tangnano9k-accel
EOF
fi

# Update sanity script gently: ensure it allows/requires new board docs if the script is readable.
if [ -f scripts/sanity_check_tree.sh ] && ! grep -q 'boards/tang-nano-9k/README.md' scripts/sanity_check_tree.sh; then
  python3 - <<'PY'
from pathlib import Path
p = Path('scripts/sanity_check_tree.sh')
s = p.read_text()
needle = 'doc/phase4_3.md'
insert = '''doc/phase4_3.md \\
  boards/tang-nano-9k/README.md \\
  boards/tang-nano-9k/variants/small.mk \\
  boards/tang-nano-9k/variants/medium.mk \\
  boards/tang-nano-9k/variants/fast-if-fits.mk \\
  boards/tang-nano-9k/variants/decrypt-small.mk \\
  hw/neorv32/tang-nano-9k/README.md \\
  hw/neorv32/tang-nano-9k/ascon_xbus_map.vh \\
  doc/phase5_2.md'''
if needle in s:
    s = s.replace(needle, insert)
p.write_text(s)
PY
fi

echo "Phase 5.2 Tang Nano 9K scaffold applied."
echo "Run:"
echo "  ./scripts/sanity_check_tree.sh"
echo "  make TANG_VARIANT=small board-tangnano9k-info"
echo "  make TANG_VARIANT=small synth-tangnano9k-accel"
echo "  make synth-tangnano9k-matrix"
