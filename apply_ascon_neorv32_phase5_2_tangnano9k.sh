cat > boards/tang-nano-9k/gowin/README.md <<'EOF'
# Gowin project placeholder

This directory is reserved for the Gowin EDA project files for the Tang Nano 9K NEORV32 build.

We are not committing a generated Gowin project yet because the exact NEORV32 top-level, memory strategy, UART pins, and clock/PLL choice still need to be locked.

Expected future files:

```text
ascon_neorv32_tangnano9k.gprj
gowin_run.tcl
impl/pnr constraints generated by Gowin EDA
```

The current Phase 5.2 targets only perform accelerator-level synthesis sizing. Full Gowin place-and-route is Phase 5.3/5.4.
EOF

cat > boards/tang-nano-9k/constraints/README.md <<'EOF'
# Constraints placeholder

Do not add guessed Tang Nano 9K pin constraints here.

The first real `.cst` file should be derived from the Sipeed schematic and the chosen NEORV32 top-level pins. At minimum we will need:

```text
27 MHz clock input
UART RX/TX through the onboard USB-UART path
reset strategy
optional LEDs for heartbeat/status
JTAG kept available for programming/debug
```

Until those signals are fixed in the top-level, this directory remains documentation-only.
EOF

cat > hw/neorv32/tang-nano-9k/README.md <<'EOF'
# NEORV32 Tang Nano 9K hardware integration

This directory is the future home of the board-specific NEORV32 top-level for Tang Nano 9K.

Current Phase 5.2 scope:

```text
- define board target strategy
- define accelerator sizing variants
- keep one XBUS accelerator as the initial hardware target
- do not yet claim a complete Gowin bitstream build
```

Planned top-level structure:

```text
Tang Nano 9K pins
  -> clock/reset/UART/JTAG wiring
  -> NEORV32 top
      -> XBUS external peripheral interface
          -> ascon_aead128_xbus
```

Initial address map:

```text
0xF000_0000  Ascon accelerator MMIO window
```

For the first board build, use a single accelerator instance. Dual encrypt/decrypt windows can be reintroduced later if resource usage allows.
EOF

